import pandas as pd
import streamlit as st
import io

def load_data(uploaded_file: st.UploadedFile) -> pd.DataFrame | None:
    """
    Loads data from an uploaded file (CSV or Excel) into a pandas DataFrame.

    Args:
        uploaded_file: The file uploaded via Streamlit's st.file_uploader.

    Returns:
        A pandas DataFrame if the file is successfully loaded, None otherwise.
    """
    if uploaded_file is None:
        return None

    file_extension = uploaded_file.name.split('.')[-1].lower()

    try:
        if file_extension == 'csv':
            df = pd.read_csv(uploaded_file)
        elif file_extension in ['xlsx', 'xls']:
            df = pd.read_excel(uploaded_file)
        else:
            st.error(f"Unsupported file type: .{file_extension}. Please upload a CSV or Excel file.")
            return None
        return df
    except Exception as e:
        st.error(f"Error loading file: {e}")
        return None
