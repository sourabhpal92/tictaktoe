import pandas as pd
import plotly.express as px
import plotly.graph_objects as go
import streamlit as st

def generate_chart(df: pd.DataFrame, chart_type: str, x_col: str, y_col: str = None, color_col: str = None) -> go.Figure | None:
    """
    Generates a Plotly chart based on the selected type and columns.

    Args:
        df: The pandas DataFrame containing the data.
        chart_type: The type of chart to generate (e.g., "Bar Chart", "Line Chart").
        x_col: The column to use for the X-axis.
        y_col: The column to use for the Y-axis (optional, required for some charts).
        color_col: The column to use for coloring/grouping (optional).

    Returns:
        A Plotly Figure object if successful, None otherwise.
    """
    if df.empty:
        st.warning("Cannot generate chart from empty data.")
        return None

    try:
        if chart_type == "Bar Chart":
            if y_col:
                # Ensure y_col is numeric for plotting
                df[y_col] = pd.to_numeric(df[y_col], errors='coerce')
                # Drop rows where y_col became NaN due to coercion
                df_filtered = df.dropna(subset=[y_col])
                if df_filtered.empty:
                    st.warning(f"No numeric data in '{y_col}' after cleaning. Cannot create bar chart.")
                    return None
                fig = px.bar(df_filtered, x=x_col, y=y_col, color=color_col, title=f"{y_col} by {x_col}")
            else:
                st.warning("Y-axis is required for Bar Chart.")
                return None
        elif chart_type == "Line Chart":
            if y_col:
                # Ensure x_col is suitable for line chart (numeric or datetime)
                # Attempt to convert x_col to datetime if possible
                try:
                    df[x_col] = pd.to_datetime(df[x_col], errors='coerce')
                    df_filtered = df.dropna(subset=[x_col])
                    if df_filtered.empty:
                        st.warning(f"No valid date/time data in '{x_col}' after cleaning. Cannot create line chart.")
                        return None
                except Exception:
                    # If not datetime, ensure it's numeric for a numeric line plot
                    df[x_col] = pd.to_numeric(df[x_col], errors='coerce')
                    df_filtered = df.dropna(subset=[x_col])
                    if df_filtered.empty:
                        st.warning(f"No numeric data in '{x_col}' after cleaning. Cannot create line chart.")
                        return None

                # Ensure y_col is numeric for plotting
                df_filtered[y_col] = pd.to_numeric(df_filtered[y_col], errors='coerce')
                df_filtered = df_filtered.dropna(subset=[y_col])
                if df_filtered.empty:
                    st.warning(f"No numeric data in '{y_col}' after cleaning. Cannot create line chart.")
                    return None

                fig = px.line(df_filtered, x=x_col, y=y_col, color=color_col, title=f"{y_col} over {x_col}")
            else:
                st.warning("Y-axis is required for Line Chart.")
                return None
        elif chart_type == "Pie Chart":
            if x_col: # x_col will be used as 'names' and y_col as 'values'
                if y_col:
                    # Ensure y_col is numeric for values
                    df[y_col] = pd.to_numeric(df[y_col], errors='coerce')
                    df_filtered = df.dropna(subset=[y_col])
                    if df_filtered.empty:
                        st.warning(f"No numeric data in '{y_col}' after cleaning. Cannot create pie chart.")
                        return None
                    fig = px.pie(df_filtered, names=x_col, values=y_col, title=f"Distribution of {y_col} by {x_col}")
                else:
                    st.warning("Y-axis is required for Pie Chart values.")
                    return None
            else:
                st.warning("X-axis (for names) is required for Pie Chart.")
                return None
        elif chart_type == "Scatter Plot":
            if x_col and y_col:
                # Ensure x_col and y_col are numeric for plotting
                df[x_col] = pd.to_numeric(df[x_col], errors='coerce')
                df[y_col] = pd.to_numeric(df[y_col], errors='coerce')
                df_filtered = df.dropna(subset=[x_col, y_col])
                if df_filtered.empty:
                    st.warning(f"No numeric data in '{x_col}' or '{y_col}' after cleaning. Cannot create scatter plot.")
                    return None
                fig = px.scatter(df_filtered, x=x_col, y=y_col, color=color_col, title=f"Scatter Plot of {y_col} vs {x_col}")
            else:
                st.warning("X-axis and Y-axis are required for Scatter Plot.")
                return None
        else:
            st.warning("Unsupported chart type selected.")
            return None

        return fig
    except Exception as e:
        st.error(f"Error generating chart: {e}")
        return None
