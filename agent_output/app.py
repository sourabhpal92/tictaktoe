import streamlit as st
import pandas as pd
import logging
import os
from datetime import datetime

# Import local utilities
from utils.config import load_config
from utils.data_loader import load_data
from utils.plotter import generate_chart

# --- Configuration and Logging Setup ---
config = load_config()
APP_TITLE = config["APP_TITLE"]
LOG_LEVEL_STR = config["LOG_LEVEL"].upper()

# Ensure logs directory exists
log_dir = "logs"
os.makedirs(log_dir, exist_ok=True)

# Map string log levels to logging module constants
LOG_LEVEL = getattr(logging, LOG_LEVEL_STR, logging.INFO)

# Setup logging
logging.basicConfig(
    level=LOG_LEVEL,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler(os.path.join(log_dir, 'app.log')),
        logging.StreamHandler()
    ]
)
logger = logging.getLogger(__name__)

logger.info(f"Application started with title: {APP_TITLE} and log level: {LOG_LEVEL_STR}")

# --- Streamlit Page Configuration ---
st.set_page_config(
    page_title=APP_TITLE,
    layout="wide",
    initial_sidebar_state="expanded"
)

# --- Initialize Session State ---
if 'uploaded_df' not in st.session_state:
    st.session_state['uploaded_df'] = pd.DataFrame()
    logger.debug("Initialized 'uploaded_df' in session state.")

# --- Sidebar Navigation ---
st.sidebar.title("Navigation")
page = st.sidebar.radio("Go to", ["Home", "Data Explorer", "AI Agent Playground"])

# --- Main Content Area ---
st.title(APP_TITLE)

if page == "Home":
    st.header("Welcome to the AI Agent Workbench!")
    st.markdown("""
        This platform serves as a versatile frontend foundation for interacting with AI agents,
        exploring datasets, and developing new AI-powered features.

        Use the sidebar to navigate:
        - **Data Explorer:** Upload and visualize your data with interactive charts.
        - **AI Agent Playground:** A dedicated space for future AI agent interactions and experiments.
    """)
    logger.info("Accessed Home page.")

elif page == "Data Explorer":
    st.header("Data Explorer")
    st.markdown("Upload a CSV or Excel file to preview your data and generate interactive charts.")

    uploaded_file = st.file_uploader("Choose a CSV or Excel file", type=["csv", "xlsx", "xls"])

    if uploaded_file is not None:
        logger.info(f"File uploaded: {uploaded_file.name}")
        df = load_data(uploaded_file)
        if df is not None:
            st.session_state['uploaded_df'] = df
            st.success("File successfully loaded!")
            logger.info(f"Loaded DataFrame shape: {df.shape}")
        else:
            st.session_state['uploaded_df'] = pd.DataFrame() # Clear df on error
            st.error("Could not load the file. Please check its format.")
            logger.error(f"Failed to load file: {uploaded_file.name}")

    df_to_display = st.session_state.get('uploaded_df', pd.DataFrame())

    if not df_to_display.empty:
        st.subheader("Data Preview")
        st.dataframe(df_to_display.head())
        st.write(f"Showing first {min(5, len(df_to_display))} rows of {len(df_to_display)} rows and {len(df_to_display.columns)} columns.")

        st.subheader("Chart Generator")
        all_columns = df_to_display.columns.tolist()

        if not all_columns:
            st.warning("No columns available to generate charts.")
            logger.warning("No columns in DataFrame for chart generation.")
        else:
            col1, col2, col3 = st.columns(3)

            with col1:
                chart_type = st.selectbox(
                    "Select Chart Type",
                    ("Bar Chart", "Line Chart", "Pie Chart", "Scatter Plot"),
                    key="chart_type"
                )
            with col2:
                x_col = st.selectbox(
                    "Select X-axis Column",
                    options=[''] + all_columns,
                    index=0,
                    key="x_axis_col"
                )
            with col3:
                y_col = st.selectbox(
                    "Select Y-axis Column",
                    options=[''] + all_columns,
                    index=0,
                    key="y_axis_col"
                )

            color_col = st.selectbox(
                "Optional: Color/Group by Column",
                options=[''] + all_columns,
                index=0,
                key="color_col"
            )

            # Filter out empty selections
            x_col_final = x_col if x_col != '' else None
            y_col_final = y_col if y_col != '' else None
            color_col_final = color_col if color_col != '' else None

            if st.button("Generate Chart"):
                logger.info(f"Attempting to generate {chart_type} with x={x_col_final}, y={y_col_final}, color={color_col_final}")
                if x_col_final is None:
                    st.warning("Please select at least an X-axis column to generate a chart.")
                else:
                    try:
                        # Attempt to generate chart using plotter utility
                        fig = generate_chart(df_to_display.copy(), chart_type, x_col_final, y_col_final, color_col_final)
                        if fig:
                            st.plotly_chart(fig, use_container_width=True)
                            logger.info(f"Successfully generated {chart_type}.")
                        else:
                            st.warning("Chart could not be generated with the selected options. Please check your column selections and data types.")
                            logger.warning(f"Failed to generate {chart_type} due to plotter returning None. Check previous warnings.")
                    except Exception as e:
                        st.error(f"An unexpected error occurred during chart generation: {e}")
                        logger.exception("Unexpected error during chart generation.")
    else:
        st.info("Upload a file above to begin data exploration.")

elif page == "AI Agent Playground":
    st.header("AI Agent Playground (Coming Soon!)")
    st.markdown("""
        This section is reserved for future enhancements where you will be able to
        interact directly with various AI agents.

        Potential features include:
        - Natural language querying of your uploaded data.
        - Automated report generation.
        - Interactive data transformations through conversational AI.
        - Integration with large language models for complex tasks.
    """)
    logger.info("Accessed AI Agent Playground page.")

logger.debug("Application rerun complete.")
