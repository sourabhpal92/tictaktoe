import pandas as pd
import os
from datetime import datetime, timedelta

def create_sample_excel_file():
    """
    Creates a sample 'sample_users.xlsx' file in the 'data' directory.
    """
    data_dir = os.path.join(os.path.dirname(__file__))
    os.makedirs(data_dir, exist_ok=True) # Ensure data directory exists

    file_path = os.path.join(data_dir, 'sample_users.xlsx')

    # Generate sample data
    num_users = 10
    users_data = {
        'User ID': [f'U{i:03d}' for i in range(1, num_users + 1)],
        'Name': [f'User {i}' for i in range(1, num_users + 1)],
        'Age': [20 + i % 40 for i in range(num_users)],
        'Registration Date': [(datetime(2022, 1, 1) + timedelta(days=i*7)).strftime('%Y-%m-%d') for i in range(num_users)],
        'Active': [True if i % 2 == 0 else False for i in range(num_users)],
        'Country': ['USA', 'Canada', 'UK', 'Germany', 'France'] * (num_users // 5) + ['USA'] * (num_users % 5)
    }

    df = pd.DataFrame(users_data)

    try:
        df.to_excel(file_path, index=False)
        print(f"Successfully created sample Excel file: {file_path}")
    except Exception as e:
        print(f"Error creating sample Excel file: {e}")

if __name__ == "__main__":
    create_sample_excel_file()
